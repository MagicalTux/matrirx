function base64toint($ts) {
	$val=0;
	for($i=0;$i<strlen($ts);$i++) {
		$val=$val<<6;
		$val+=$GLOBALS['base64_to_int_map'][ord($ts{$i})];
	}
	return $val;
}

function base64_ts_convert($ts) {
	if ($ts{0}!='!') return $ts;
	return base64toint(substr($ts,1));
}
